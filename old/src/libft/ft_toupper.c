#include "libft.h"

int		ft_toupper(int c)
{
	if ((c <= 122) && (c >= 97))
		c -= 32;
	return (c);
}

char	*ft_str_toupper(char *s)
{
	int i;

	i = 0;
	while (s[i])
	{
		s[i] = ft_toupper(s[i]);
		i++;
	}
	return (s);
}
